# 초성, 중성, 종성 리스트
INITIAL_LIST = [0x1100, 0x1101, 0x1102, 0x1103, 0x1104, 0x1105, 0x1106,
                0x1107, 0x1108, 0x1109, 0x110A, 0x110B, 0x110C, 0x110D, 0x110E,
                0x110F, 0x1110, 0x1111, 0x1112]
NEUTRALITY_LIST = [0x1161, 0x1162, 0x1163, 0x1164, 0x1165, 0x1166, 0x1167,
                 0x1168, 0x1169, 0x116A, 0x116B, 0x116C, 0x116D, 0x116E,
                 0x116F, 0x1170, 0x1171, 0x1172, 0x1173, 0x1174, 0x1175]
FINAL_LIST = [None, 0x11A8, 0x11A9, 0x11AA, 0x11AB, 0x11AC, 0x11AD,
                 0x11AE, 0x11AF, 0x11B0, 0x11B1, 0x11B2, 0x11B3, 0x11B4, 0x11B5,
                 0x11B6, 0x11B7, 0x11B8, 0x11B9, 0x11BA, 0x11BB, 0x11BC, 0x11BD,
                 0x11BE, 0x11BF, 0x11C0, 0x11C1, 0x11C2]

def combine(initial_index, neutrality_index, final_index=None):

    if final_index is None:
        final_index = 0

    # 유니코드 계산
    unicode_value = 0xAC00 + (initial_index * 21 * 28) + (neutrality_index * 28) + final_index
    return chr(unicode_value)

# '가' 만들기
initial_index = INITIAL_LIST.index(0x1100)  # 'ㄱ'
neutrality_index = NEUTRALITY_LIST.index(0x1161)  # 'ㅏ'
final_index = None  # 종성 없음

# 변환 실행
result_hangul = combine(initial_index, neutrality_index, final_index)
print(result_hangul)  # 가
